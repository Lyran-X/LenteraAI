    def _recommended_review(self, weak_topics: list[str], language: str = "id") -> list[str]:
        if language == "en":
            return [f"Review {topic} with AI Tutor, then retry a short focused quiz." for topic in weak_topics]
        return [f"Pelajari lagi {topic} dengan AI Tutor, lalu coba ulang quiz singkat yang lebih fokus." for topic in weak_topics]

    def _normalize_question_type(self, value: object) -> str:
        normalized = str(value or "multiple_choice").strip().lower()
        return normalized if normalized in {"multiple_choice", "true_false", "short_answer"} else "multiple_choice"

    def _db_difficulty(self, value: object) -> str:
        normalized = str(value or "medium").strip().lower()
        if normalized == "adaptive":
            return "mixed"
        return normalized if normalized in {"easy", "medium", "hard", "mixed"} else "medium"

    def _normalize_options(self, options: object, question_type: str) -> list[str]:
        if question_type == "short_answer":
            return []
        if question_type == "true_false":
            if isinstance(options, list):
                cleaned = [self._normalize_answer_value(option) for option in options if self._normalize_answer_value(option)]
                if len(cleaned) >= 2:
                    lowered = {item.lower() for item in cleaned[:2]}
                    if lowered & {"true", "benar"} and lowered & {"false", "salah"}:
                        return cleaned[:2]
            return ["True", "False"]
        if not isinstance(options, list):
            return []
        return [self._normalize_answer_value(option) for option in options if self._normalize_answer_value(option)]

    def _options_as_strings(self, options: object) -> list[str]:
        if not isinstance(options, list):
            return []
        return [self._normalize_answer_value(option) for option in options]

    def _normalize_answer_value(self, value: object) -> str:
        if isinstance(value, dict):
            value = value.get("value") or value.get("answer") or value.get("text")
        return str(value or "").strip()

    def _answer_value(self, value: object) -> str:
        if isinstance(value, dict):
            return self._normalize_answer_value(value.get("value"))
        return self._normalize_answer_value(value)

    def _correct_answer_value(self, question: QuizQuestion) -> str:
        return self._normalize_answer_value(question.correct_answer)

    def _is_correct(self, user_answer: str, correct_answer: str) -> bool:
        return self._normalized_for_scoring(user_answer) == self._normalized_for_scoring(correct_answer)

    def _normalized_for_scoring(self, value: str) -> str:
        return re.sub(r"\s+", " ", str(value or "").strip().lower())


