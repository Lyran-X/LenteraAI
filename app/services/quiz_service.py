class QuizService:
    def generate_quiz(self, db: Session, current_user: User, payload: QuizGenerateRequest) -> QuizRead:
        source_document = self._get_source_document(db, current_user, payload)
        document_context = self._build_document_context(db, source_document, payload) if source_document else None
        effective_question_count = self._effective_question_count(payload, document_context)
        effective_difficulty_distribution = self._difficulty_distribution(payload.difficulty.value, effective_question_count)
        self._log_document_generation_context(
            source_document,
            document_context,
            payload,
            effective_question_count,
            effective_difficulty_distribution,
        )

        system_prompt, user_prompt = self._build_generation_prompt(
            payload,
            source_document,
            document_context,
            effective_question_count,
            effective_difficulty_distribution,
        )
        llm_result = llm_service.generate_text_response(system_prompt=system_prompt, user_prompt=user_prompt)
        active_llm_result = llm_result
        generation_message = "Quiz generated by LLM."
        validation_errors: list[str] = []
        quiz_data: dict[str, Any] | None = None

        if llm_result.metadata.get("fallback"):
            validation_errors.append(llm_result.content)
        else:
            try:
                quiz_data = self._parse_llm_quiz(
                    llm_result.content,
                    payload,
                    source_document,
                    effective_question_count,
                    document_context,
                    effective_difficulty_distribution,
                )
            except ValueError as exc:
                validation_errors.append(str(exc))
                logger.warning(
                    "Quiz LLM output failed validation source_type=%s document_id=%s errors=%s",
                    payload.source_type.value,
                    str(source_document.id) if source_document else None,
                    validation_errors,
                )

        if quiz_data is None and not llm_result.metadata.get("fallback"):
            repaired_quiz, repair_result, repair_errors = self._try_repair_quiz_generation(
                payload=payload,
                document=source_document,
                document_context=document_context,
                invalid_content=llm_result.content,
                validation_errors=validation_errors,
                question_count=effective_question_count,
                difficulty_distribution=effective_difficulty_distribution,
            )
            validation_errors.extend(repair_errors)
            if repaired_quiz is not None and repair_result is not None:
                quiz_data = repaired_quiz
                active_llm_result = repair_result
                generation_message = "Quiz generated by LLM repair."

        if quiz_data is None:
            quiz_data = self._fallback_quiz(payload, source_document, document_context, effective_question_count)
            reason = "; ".join(validation_errors) if validation_errors else "LLM output unavailable or invalid."
            generation_message = f"Development fallback used: {reason}"

        logger.info(
            "Quiz generation completed source_type=%s document_id=%s question_type=%s requested_difficulty=%s effective_difficulty_distribution=%s generated_question_count=%s validation_errors=%s",
            payload.source_type.value,
            str(source_document.id) if source_document else None,
            payload.question_type.value,
            payload.difficulty.value,
            effective_difficulty_distribution,
            len(quiz_data.get("questions", [])),
            validation_errors,
        )
        db_difficulty = self._db_difficulty(payload.difficulty.value)
        quiz_topic = self._resolved_topic(payload, source_document, quiz_data)
        context_chunk_ids = document_context.get("chunk_ids", []) if document_context else []
        source_id = source_document.id if source_document else payload.source_id
        quiz = Quiz(
            user_id=current_user.id,
            source_type=payload.source_type.value,
            source_document_id=source_document.id if source_document else None,
            title=quiz_data["title"],
            topic=quiz_topic,
            description=quiz_data.get("description"),
            difficulty=db_difficulty,
            status="published",
            total_questions=len(quiz_data["questions"]),
            generated_by_ai=True,
            model_name=active_llm_result.model,
            metadata_={
                "question_type": payload.question_type.value,
                "requested_difficulty": payload.difficulty.value,
                "language": payload.language,
                "source_id": str(source_id) if source_id else None,
                "document_id": str(source_document.id) if source_document else None,
                "document_title": source_document.title if source_document else None,
                "focus_topic": payload.focus_topic,
                "context_chunk_ids": [str(chunk_id) for chunk_id in context_chunk_ids],
                "llm_provider": active_llm_result.provider,
                "llm_model": active_llm_result.model,
                "generation_message": generation_message,
                "context_length": document_context.get("context_length") if document_context else None,
                "requested_total_questions": payload.total_questions,
                "effective_total_questions": effective_question_count,
                "effective_difficulty_distribution": effective_difficulty_distribution,
            },
        )
        db.add(quiz)
        db.flush()

        for index, question_data in enumerate(quiz_data["questions"], start=1):
            question_type = self._normalize_question_type(question_data.get("question_type") or payload.question_type.value)
            difficulty = self._db_difficulty(question_data.get("difficulty") or payload.difficulty.value)
            db.add(
                QuizQuestion(
                    quiz_id=quiz.id,
                    question_order=index,
                    question_type=question_type,
                    question_text=question_data["question_text"],
                    options=self._normalize_options(question_data.get("options"), question_type),
                    correct_answer={"value": self._normalize_answer_value(question_data.get("correct_answer"))},
                    explanation=question_data.get("explanation"),
                    topic=question_data.get("topic") or quiz_topic,
                    difficulty=difficulty,
                    source_chunk_ids=context_chunk_ids,
                    metadata_={"requested_difficulty": payload.difficulty.value, "focus_topic": payload.focus_topic, "language": payload.language},
                )
            )

        db.commit()
        db.refresh(quiz)
        return self.get_quiz(db=db, current_user=current_user, quiz_id=quiz.id)

    def get_quizzes(self, db: Session, current_user: User) -> list[QuizRead]:
        quizzes = db.scalars(
            select(Quiz)
            .options(selectinload(Quiz.questions))
            .where(Quiz.user_id == current_user.id)
            .order_by(Quiz.created_at.desc())
        ).all()
        return [self._to_quiz_read(quiz) for quiz in quizzes]

    def get_quiz(self, db: Session, current_user: User, quiz_id: uuid.UUID) -> QuizRead:
        quiz = db.scalar(
            select(Quiz)
            .options(selectinload(Quiz.questions))
            .where(Quiz.id == quiz_id, Quiz.user_id == current_user.id)
        )
        if not quiz:
            raise HTTPException(status_code=status.HTTP_404_NOT_FOUND, detail="Quiz not found.")
        return self._to_quiz_read(quiz)

    def submit_quiz(
        self,
        db: Session,
        current_user: User,
        quiz_id: uuid.UUID,
        payload: QuizSubmitRequest,
    ) -> QuizAttemptRead:
        quiz = db.scalar(
            select(Quiz)
            .options(selectinload(Quiz.questions))
            .where(Quiz.id == quiz_id, Quiz.user_id == current_user.id)
        )
        if not quiz:
            raise HTTPException(status_code=status.HTTP_404_NOT_FOUND, detail="Quiz not found.")

        questions_by_id = {question.id: question for question in quiz.questions}
        submitted_answers = {answer.question_id: str(answer.user_answer).strip() for answer in payload.answers}
        now = datetime.now(UTC)
        total_questions = len(quiz.questions)
        correct_count = 0
        answer_results: list[QuizAnswerResult] = []
        wrong_topics: list[str] = []

        attempt = QuizAttempt(
            quiz_id=quiz.id,
            user_id=current_user.id,
            status="submitted",
            submitted_at=now,
            graded_at=now,
            max_score=Decimal("100"),
            weak_topics=[],
            metadata_={},
        )
        db.add(attempt)
        db.flush()

        quiz_language = str((quiz.metadata_ or {}).get("language") or "id")

        for question in sorted(quiz.questions, key=lambda item: item.question_order):
            user_answer = submitted_answers.get(question.id, "")
            correct_answer = self._correct_answer_value(question)
            question_type = self._normalize_question_type(question.question_type)

            if question_type == "short_answer":
                evaluation = self.evaluate_short_answer(
                    user_answer=user_answer,
                    correct_answer=correct_answer,
                    explanation=question.explanation,
                    question_text=question.question_text,
                    context=question.topic,
                    question_id=question.id,
                    language=quiz_language,
                )
            else:
                is_exact_correct = self._is_correct_by_question_type(user_answer, correct_answer, question_type)
                evaluation = {
                    "is_correct": is_exact_correct,
                    "similarity_score": None,
                    "feedback": question.explanation or self._exact_answer_feedback(is_exact_correct, quiz_language),
                    "llm_evaluator_used": False,
                }

            is_correct = bool(evaluation["is_correct"])
            feedback = str(evaluation.get("feedback") or question.explanation or "")
            similarity_score = evaluation.get("similarity_score") if question_type == "short_answer" else None

            if is_correct:
                correct_count += 1
            elif question.topic:
                wrong_topics.append(question.topic)

            db.add(
                QuizAnswer(
                    attempt_id=attempt.id,
                    question_id=question.id,
                    user_id=current_user.id,
                    answer={
                        "value": user_answer,
                        "similarity_score": similarity_score,
                        "rule_similarity": evaluation.get("rule_similarity"),
                        "keyword_overlap": evaluation.get("keyword_overlap"),
                        "llm_evaluator_used": evaluation.get("llm_evaluator_used", False),
                    },
                    is_correct=is_correct,
                    score_awarded=Decimal("1") if is_correct else Decimal("0"),
                    feedback=feedback,
                )
            )
            answer_results.append(
                QuizAnswerResult(
                    question_id=question.id,
                    question_text=question.question_text,
                    user_answer=user_answer,
                    correct_answer=correct_answer,
                    is_correct=is_correct,
                    similarity_score=similarity_score,
                    feedback=feedback,
                    explanation=question.explanation,
                    topic=question.topic,
                )
            )
        wrong_count = total_questions - correct_count
        score = round((correct_count / total_questions) * 100, 2) if total_questions else 0.0
        weak_topics = sorted(set(wrong_topics))
        recommended_review = self._recommended_review(weak_topics, quiz_language)

        attempt.score = Decimal(str(score))
        attempt.percentage = Decimal(str(score))
        attempt.weak_topics = weak_topics
        attempt.ai_feedback = "; ".join(recommended_review) if recommended_review else ("Kerja bagus. Pertahankan latihanmu." if quiz_language == "id" else "Great work. Keep practicing.")
        attempt.metadata_ = {
            "total_questions": total_questions,
            "correct_count": correct_count,
            "wrong_count": wrong_count,
            "recommended_review": recommended_review,
            "language": quiz_language,
        }

        db.commit()
        db.refresh(attempt)
        return self._to_attempt_read(attempt, quiz=quiz, answer_results=answer_results)

