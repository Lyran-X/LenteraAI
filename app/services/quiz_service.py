    def _to_attempt_read(
        self,
        attempt: QuizAttempt,
        quiz: Quiz | None = None,
        answer_results: list[QuizAnswerResult] | None = None,
    ) -> QuizAttemptRead:
        metadata = attempt.metadata_ or {}
        quiz_obj = quiz or attempt.quiz
        if answer_results is None:
            answer_results = [self._answer_to_result(answer) for answer in sorted(attempt.answers or [], key=lambda item: item.created_at)]
        return QuizAttemptRead(
            id=attempt.id,
            quiz_id=attempt.quiz_id,
            quiz_title=quiz_obj.title if quiz_obj else None,
            score=float(attempt.percentage if attempt.percentage is not None else attempt.score),
            total_questions=int(metadata.get("total_questions") or len(answer_results)),
            correct_count=int(metadata.get("correct_count") or sum(1 for answer in answer_results if answer.is_correct)),
            wrong_count=int(metadata.get("wrong_count") or sum(1 for answer in answer_results if not answer.is_correct)),
            weak_topics=attempt.weak_topics or [],
            recommended_review=metadata.get("recommended_review") or self._recommended_review(attempt.weak_topics or [], str(metadata.get("language") or "id")),
            answers=answer_results,
            started_at=attempt.started_at,
            submitted_at=attempt.submitted_at,
            created_at=attempt.created_at,
            updated_at=attempt.updated_at,
        )

    def _answer_to_result(self, answer: QuizAnswer) -> QuizAnswerResult:
        question = answer.question
        answer_payload = answer.answer or {}
        similarity_score = None
        if isinstance(answer_payload, dict) and answer_payload.get("similarity_score") is not None:
            similarity_score = float(answer_payload["similarity_score"])

        return QuizAnswerResult(
            question_id=answer.question_id,
            question_text=question.question_text if question else "",
            user_answer=self._answer_value(answer.answer),
            correct_answer=self._correct_answer_value(question) if question else "",
            is_correct=bool(answer.is_correct),
            similarity_score=similarity_score,
            feedback=answer.feedback,
            explanation=question.explanation if question else answer.feedback,
            topic=question.topic if question else None,
        )

    def evaluate_short_answer(
        self,
        *,
        user_answer: str,
        correct_answer: str,
        explanation: str | None = None,
        question_text: str | None = None,
        context: str | None = None,
        question_id: uuid.UUID | None = None,
        language: str = "id",
    ) -> dict[str, Any]:
        answer_language = language if language in {"id", "en"} else "id"
        normalized_user = self._normalize_semantic_text(user_answer)
        normalized_correct = self._normalize_semantic_text(correct_answer)

        if not normalized_user or len(normalized_user) < 6:
            result = {
                "is_correct": False,
                "similarity_score": 0,
                "rule_similarity": 0.0,
                "keyword_overlap": 0.0,
                "llm_evaluator_used": False,
                "feedback": self._short_answer_feedback("empty", answer_language),
            }
            self._log_short_answer_scoring(question_id, result)
            return result

        if not normalized_correct:
            result = {
                "is_correct": False,
                "similarity_score": 0,
                "rule_similarity": 0.0,
                "keyword_overlap": 0.0,
                "llm_evaluator_used": False,
                "feedback": self._short_answer_feedback("missing_expected", answer_language),
            }
            self._log_short_answer_scoring(question_id, result)
            return result

        rule_similarity = SequenceMatcher(None, normalized_user, normalized_correct).ratio()
        keyword_source = " ".join([correct_answer or "", explanation or ""])
        important_keywords = self._semantic_keywords(keyword_source)
        if len(important_keywords) < 3:
            important_keywords = self._semantic_keywords(" ".join([correct_answer or "", question_text or "", context or ""]))
        user_keywords = set(self._semantic_keywords(user_answer))
        matched_keywords = [keyword for keyword in important_keywords if keyword in user_keywords or keyword in normalized_user]
        keyword_overlap = len(matched_keywords) / max(len(important_keywords), 1)
        contains_core_keywords = (
            (len(matched_keywords) >= min(3, max(len(important_keywords), 1)) and keyword_overlap >= 0.45)
            or (len(matched_keywords) >= 4 and keyword_overlap >= 0.35)
        )
        rule_score = round(max(rule_similarity, keyword_overlap) * 100)

        if rule_similarity >= 0.65 or keyword_overlap >= 0.55 or contains_core_keywords:
            result = {
                "is_correct": True,
                "similarity_score": max(rule_score, 70),
                "rule_similarity": round(rule_similarity, 3),
                "keyword_overlap": round(keyword_overlap, 3),
                "llm_evaluator_used": False,
                "feedback": self._short_answer_feedback("correct", answer_language),
            }
            self._log_short_answer_scoring(question_id, result)
            return result

        if rule_score < 35 or len(user_keywords) < 2:
            result = {
                "is_correct": False,
                "similarity_score": rule_score,
                "rule_similarity": round(rule_similarity, 3),
                "keyword_overlap": round(keyword_overlap, 3),
                "llm_evaluator_used": False,
                "feedback": self._short_answer_feedback("weak", answer_language),
            }
            self._log_short_answer_scoring(question_id, result)
            return result

        llm_result = self._evaluate_short_answer_with_llm(
            user_answer=user_answer,
            correct_answer=correct_answer,
            explanation=explanation,
            question_text=question_text,
            rule_similarity=rule_similarity,
            keyword_overlap=keyword_overlap,
            language=answer_language,
        )
        if llm_result:
            self._log_short_answer_scoring(question_id, llm_result)
            return llm_result

        result = {
            "is_correct": rule_score >= 55,
            "similarity_score": rule_score,
            "rule_similarity": round(rule_similarity, 3),
            "keyword_overlap": round(keyword_overlap, 3),
            "llm_evaluator_used": False,
            "feedback": self._short_answer_feedback("partial", answer_language) if rule_score < 55 else self._short_answer_feedback("mostly_correct", answer_language),
        }
        self._log_short_answer_scoring(question_id, result)
        return result

    def _evaluate_short_answer_with_llm(
        self,
        *,
        user_answer: str,
        correct_answer: str,
        explanation: str | None,
        question_text: str | None,
        rule_similarity: float,
        keyword_overlap: float,
        language: str,
    ) -> dict[str, Any] | None:
        feedback_language = "Indonesian" if language == "id" else "English"
        system_prompt = (
            "Evaluate whether the student's answer is semantically correct compared to the expected answer. "
            "Do not require exact wording. Accept paraphrases. Mark correct if the core meaning is captured. "
            f"Use {feedback_language} feedback. Return JSON only."
        )
        user_prompt = json.dumps(
            {
                "question": question_text,
                "language": language,
                "expected_answer": correct_answer,
                "student_answer": user_answer,
                "reference_explanation": explanation,
                "rule_similarity": round(rule_similarity, 3),
                "keyword_overlap": round(keyword_overlap, 3),
                "required_schema": {
                    "is_correct": "boolean",
                    "score": "integer 0-100",
                    "feedback": "short feedback string",
                },
            },
            ensure_ascii=False,
        )
        result = llm_service.generate_text_response(system_prompt=system_prompt, user_prompt=user_prompt)
        if result.metadata.get("fallback"):
            return None

        try:
            parsed = json.loads(self._extract_json(result.content))
        except (json.JSONDecodeError, ValueError):
            logger.warning("Short answer LLM evaluator returned invalid JSON.")
            return None

        score = max(0, min(100, int(parsed.get("score") or 0)))
        raw_is_correct = parsed.get("is_correct")
        if isinstance(raw_is_correct, bool):
            is_correct = raw_is_correct
        else:
            is_correct = str(raw_is_correct).strip().lower() in {"true", "yes", "ya", "benar", "correct"}

        return {
            "is_correct": is_correct and score >= 55,
            "similarity_score": score,
            "rule_similarity": round(rule_similarity, 3),
            "keyword_overlap": round(keyword_overlap, 3),
            "llm_evaluator_used": True,
            "feedback": self._compact_text(parsed.get("feedback") or self._short_answer_feedback("llm_default", language), 260),
        }

    def _log_short_answer_scoring(self, question_id: uuid.UUID | None, result: dict[str, Any]) -> None:
        logger.info(
            "Short answer scoring question_id=%s question_type=short_answer rule_similarity=%s keyword_overlap=%s similarity_score=%s llm_evaluator_used=%s is_correct=%s",
            str(question_id) if question_id else None,
            result.get("rule_similarity"),
            result.get("keyword_overlap"),
            result.get("similarity_score"),
            result.get("llm_evaluator_used"),
            result.get("is_correct"),
        )

    def _normalize_semantic_text(self, value: str) -> str:
        lowered = str(value or "").lower()
        lowered = re.sub(r"[^a-z0-9A-ZÀ-ÿ\s]", " ", lowered)
        lowered = re.sub(r"\s+", " ", lowered).strip()
        return lowered

    def _semantic_keywords(self, value: str) -> list[str]:
        stopwords = self._short_answer_stopwords()
        normalized = self._normalize_semantic_text(value)
        tokens = [token for token in normalized.split() if len(token) > 3 and token not in stopwords]
        seen: set[str] = set()
        keywords: list[str] = []
        for token in tokens:
            if token not in seen:
                seen.add(token)
                keywords.append(token)
        return keywords[:18]

    def _short_answer_stopwords(self) -> set[str]:
        return {
            "yang", "dan", "atau", "dari", "adalah", "untuk", "dengan", "pada", "dalam", "sebagai", "karena", "bisa", "dapat", "akan", "tidak", "tanpa", "salah", "satu", "suatu", "yaitu", "merupakan", "secara", "pengguna", "penggunanya",
            "the", "is", "are", "and", "of", "to", "a", "an", "in", "on", "for", "with", "from", "that", "this", "can", "able", "without", "one", "into",
        }

    def _is_correct_by_question_type(self, user_answer: str, correct_answer: str, question_type: str) -> bool:
        if question_type == "true_false":
            return self._normalized_true_false(user_answer) == self._normalized_true_false(correct_answer)
        return self._is_correct(user_answer, correct_answer)

    def _normalized_true_false(self, value: str) -> str:
        normalized = self._normalized_for_scoring(value)
        if normalized in {"true", "t", "yes", "y", "ya", "benar", "betul", "1"}:
            return "true"
        if normalized in {"false", "f", "no", "n", "tidak", "salah", "0"}:
            return "false"
        return normalized
